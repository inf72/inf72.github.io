                          <h3>What do mechatronics professionals do?</h3>
                          <p><b>Mechatronics specialists </b>can do quite a bit across multiple disciplines and have a solid base from which to grow. Mechatronics specialists know both mechanical and electrical engineering fundamentals—they speak both languages—so a mechatronics specialist can work with both mechanical and engineering teams. Mechatronics specialists work with massive industrial robots, smaller robots in pick-and-place operations, control systems for bottling or packaging of food and drink products, drones, designing control systems for rides in amusement parks, prototype development. Some mechatronics specialists are employed in firms where it is necessary to design and maintain automatic equipment. This includes industries such as manufacturing, mining, aviation, robotics, defense, and transport. Other mechatronic specialists are employed by large manufacturing companies involved in high-volume production. Many new career opportunities are on the horizon due to technological advances.</p>

                          <h3>What tasks do mechatronics specialists do?</h3>
                          <p>There are many tasks mechatronic specialists perform, depending on their particular industry. Some design, develop, maintain, and manage high-technology engineering systems for the automation of industrial tasks. Others apply mechatronic/automated solutions to the transfer of material components or finished goods, or design and assist with the manufacture of consumer products such as cameras and video recorders. Still others carry out studies into the feasibility, cost implications, and performance benefits of new mechatronic equipment. And others apply electronic and mechanical processes and computers to tasks where the use of human labor may be dangerous (for example, underwater exploration, mining, or forestry).</p>
                        
                          <h3>How much do mechatronics engineers earn?</h3>
                          <p>ccording to Recruiter, the mean salary for a mechatronics engineer is <b>$88,800</b> with the top ten percent earning close to <b>$103,380</b>.</p>

